package com.knowledgebase.sopviewer;

public class Article {
    private int id;
    private String title;
    private String content;
    private String category;

    // Getters
    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }

    public String getCategory() {
        return category;
    }
}
